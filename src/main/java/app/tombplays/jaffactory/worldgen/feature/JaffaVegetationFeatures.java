public class JaffaVegetationFeatures {

    public static void bootstrap(BootstrapContext<ConfiguredFeature<?, ?>> context)
    {
        HolderGetter<PlacedFeature> placedFeatureGetter = context.lookup(Registries.PLACED_FEATURE);

        final Holder<PlacedFeature> FLOWERING_OAK_TREE_BEES_CHECKED = placedFeatureGetter.getOrThrow(JaffaTreePlacements.JAFFA_TREE_BEES_CHECKED);
        final Holder<PlacedFeature> FLOWERING_OAK_TREE_CHECKED = placedFeatureGetter.getOrThrow(JaffaTreePlacements.JAFFA_TREE_CHECKED);
    }

    private static <FC extends FeatureConfiguration, F extends Feature<FC>> void register(BootstrapContext<ConfiguredFeature<?, ?>> context, ResourceKey<ConfiguredFeature<?, ?>> configuredFeatureKey, F feature, FC configuration)
    {
        context.register(configuredFeatureKey, new ConfiguredFeature<>(feature, configuration));
    }
}
